public class ForLoop {
 
    public static void main(String a[]){
        
        for(int i=1;i<=10;i++){
            System.out.print(i+" ");
        }
        System.out.println();
        
        for(int i=1;i<=10;i=i+2){
            System.out.print(i+" ");
        }
        System.out.println();
       
        for(int i=10;i>0;i--){
            System.out.print(i+" ");
        }
        System.out.println();
    }
}